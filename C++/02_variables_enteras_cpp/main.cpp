#include <iostream>

using namespace std;

int main()
{
    int a, b, c, d, e, f;

    a = 10;
    b = 20;
    c = a+b;
    d = a-b;
    e = a*b;
    f = a/b;

    cout << "Esto es a: " << a << endl;
    cout << "Esto es b: " << b << endl;
    cout << "Esto es a + b: " << c << endl;
    cout << "Esto es a - b: " << d << endl;
    cout << "Esto es a * b: " << e << endl;
    cout << "Esto es a / b: " << f << endl;

    return 0;
}
